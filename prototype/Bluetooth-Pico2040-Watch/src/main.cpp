void setup()
{
  Serial.begin(115200); // serial monitor
  Serial1.begin(9600);  // serial hardware bus -> HC-06 pins 0 (TXD) and 1 (RXD)

  // wait for serial
  delay(1000);

  myWire.begin();

  // example code below

  if (!display.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS))
  {
    Serial.println(F("SSD1306 not found"));
    while (1)
      ;
  }

  display.clearDisplay();

  // example code above
}

int counter = 0;
unsigned long incrementCounterInterval = 1000;
unsigned long lastIncrementTime = 0;

unsigned long catTime = 10000;

unsigned long askForTimeInterval = 2000;
unsigned long laskAskTime = 0;

void loop()
{
  unsigned long currentTime = millis();

  if (currentTime - lastPingTime >= pingInterval)
  {
    lastPingTime = currentTime;
    Serial1.println("Hello World");
  }

  if (Serial1.available() > 0)
  {
    char incomingByte = Serial1.read();
    Serial.print(incomingByte);
  }

  if (currentTime - laskAskTime >= askForTimeInterval)
  {
    laskAskTime = currentTime;
    Serial1.println("GET_TIME");
  }

  if (currentTime >= catTime)
  {
    screen_imageScreen(catImg_width, catImg_height, catImg);
  }
  else if (currentTime - lastIncrementTime >= incrementCounterInterval)
  {
    lastIncrementTime = currentTime;
    screen_greetingScreen("[User]", String(counter++));
  }
}

void screen_greetingScreen(String name, String time)
{
  display.clearDisplay();

  display.setTextColor(SSD1306_WHITE);
  display.setFont(&TomThumb);
  display.setTextSize(1);
  display.setCursor(0, 10);
  display.print("Good Morning ");
  display.print(name);
  display.println("!");

  // display.setCursor(0, 25);
  display.print("Current Time: ");
  display.println(time);

  display.display();
}

void screen_imageScreen(const int width, const int height, const unsigned char image[])
{
  display.clearDisplay();
  display.drawBitmap(0, 0, image, width, height, SSD1306_WHITE);
  display.display();
}